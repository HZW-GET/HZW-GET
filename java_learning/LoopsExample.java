// 示例：循环
public class LoopsExample {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            System.out.println("for 循环: " + i);
        }

        int j = 0;
        while (j < 5) {
            System.out.println("while 循环: " + j);
            j++;
        }
    }
}